package ClasesObjectes;


public class Objetos {

	private  String desc;
	private  int habitacio;
	
	public Objetos(int habitacio, String desc) {
		this.desc = desc;
		this.habitacio = habitacio;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getHabitacion() {
		return habitacio;
	}

	public void setHabitacio(int habitacio) {
		this.habitacio = habitacio;
	}


	
}
